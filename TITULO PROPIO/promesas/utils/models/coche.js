class Coche {
  marca;
  modelo;
  cv;
  disponibilidad;
  accesorios;
  valor;

  constructor(marca, modelo, cv, disponibilidad, valor, accesorios) {
    this.marca = marca;
    this.modelo = modelo;
    this.cv = cv;
    this.valor = valor;
    this.accesorios = accesorios;
    this.disponibilidad = disponibilidad;
  }
}
